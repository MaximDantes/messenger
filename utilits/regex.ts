const regex = {
    email: /^(([^<>()[\]\.,;:\s@\"]+(\.[^<>()[\]\.,;:\s@\"]+)*)|(\".+\"))@(([^<>()[\]\.,;:\s@\"]+\.)+[^<>()[\]\.,;:\s@\"]{2,})$/i,
    phone: /^\+\d{9,15}$/,
    securePassword: new RegExp('^(?=.{14,})(?=.*[A-Z,А-ЯЁ])(?=.*[a-z,а-яё])(?=.*[0-9])(?=.*\\W).*$'),
    mediumPassword: new RegExp('^(?=.{10,})(((?=.*[A-Z,А-ЯЁ])(?=.*[a-z,а-яё]))|((?=.*[A-Z,А-ЯЁ])(?=.*[0-9]))|((?=.*[a-z,а-яё])(?=.*[0-9]))).*$'),
}

export default regex