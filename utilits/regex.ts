const regex = {
    email: /^(([^<>()[\]\.,;:\s@\"]+(\.[^<>()[\]\.,;:\s@\"]+)*)|(\".+\"))@(([^<>()[\]\.,;:\s@\"]+\.)+[^<>()[\]\.,;:\s@\"]{2,})$/i,
    phone: /\+\d{9}/,
    securePassword: new RegExp('^(?=.{14,})(?=.*[A-Z,А-ЯЁ])(?=.*[a-z,а-яё])(?=.*[0-9])(?=.*\\W).*$', 'g'),
    mediumPassword: new RegExp('^(?=.{10,})(((?=.*[A-Z,А-ЯЁ])(?=.*[a-z,а-яё]))|((?=.*[A-Z,А-ЯЁ])(?=.*[0-9]))|((?=.*[a-z,а-яё])(?=.*[0-9]))).*$', 'g'),
}

export default regex